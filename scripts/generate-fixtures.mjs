// Generates the language-agnostic conformance corpus in spec/fixtures/ by
// running the real jest spec files (unmodified) with the public API wrapped
// in recording proxies. Every `promql.*` call and every `Expression` usage
// the suite makes becomes a fixture case: { fn, params, expected | error }.
//
// Other implementations of the tsqtsq API (currently jsonnet/) replay these
// fixtures to prove behavioural equivalence with the TypeScript source of
// truth. Regenerate with: pnpm run generate:fixtures
import * as esbuild from 'esbuild';
import { mkdirSync, readdirSync, writeFileSync, rmSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');

// prettify is TypeScript-only (it wraps the lezer PromQL parser) and is
// deliberately out of scope for the cross-language conformance corpus.
const EXCLUDED_SPECS = new Set(['prettify.spec.ts']);

const specs = [
  ...readdirSync(join(root, 'src/test'))
    .filter((f) => f.endsWith('.spec.ts') && !EXCLUDED_SPECS.has(f))
    .sort()
    .map((f) => ({ suite: f.replace('.spec.ts', ''), path: `./src/test/${f}` })),
  { suite: 'expression', path: './src/expression.spec.ts' },
];

const entry = [
  `import './scripts/lib/jest-shim';`,
  `import { setSuite, recorded } from './scripts/lib/recorder-state';`,
  `import { stats } from './scripts/lib/jest-shim';`,
  ...specs.map(({ suite, path }) => `setSuite(${JSON.stringify(suite)});\nawait import(${JSON.stringify(path)});`),
  `export { recorded, stats };`,
].join('\n');

// The spec files import the implementation under test; redirect those imports
// to the recording wrappers. Everything else resolves normally.
const redirectToRecorder = {
  name: 'redirect-to-recorder',
  setup(build) {
    build.onResolve({ filter: /^\.\.?\/promql$/ }, (args) =>
      args.importer.endsWith('.spec.ts') ? { path: join(root, 'scripts/lib/record-promql.ts') } : undefined
    );
    build.onResolve({ filter: /^\.\.?\/expression$/ }, (args) =>
      args.importer.endsWith('.spec.ts') ? { path: join(root, 'scripts/lib/record-expression.ts') } : undefined
    );
  },
};

const outDir = join(root, 'node_modules/.cache/tsqtsq-fixtures');
const outFile = join(outDir, 'record.mjs');
mkdirSync(outDir, { recursive: true });

await esbuild.build({
  stdin: { contents: entry, resolveDir: root, loader: 'ts' },
  bundle: true,
  platform: 'node',
  format: 'esm',
  outfile: outFile,
  plugins: [redirectToRecorder],
  logLevel: 'silent',
});

const { recorded, stats } = await import(pathToFileURL(outFile).href);
rmSync(outDir, { recursive: true, force: true });

const fixtures = {
  '//': 'GENERATED by scripts/generate-fixtures.mjs from the jest suite -- DO NOT EDIT.',
  cases: recorded,
};

mkdirSync(join(root, 'spec/fixtures'), { recursive: true });
writeFileSync(join(root, 'spec/fixtures/conformance.json'), JSON.stringify(fixtures, null, 2) + '\n');

const errors = recorded.filter((c) => c.error !== undefined).length;
console.log(
  `ran ${stats.assertions} assertions, recorded ${recorded.length} conformance cases ` +
    `(${recorded.length - errors} expressions, ${errors} errors) -> spec/fixtures/conformance.json`
);
